int		ft_atoi(char *str)
{
	int ret;
	int i;
	int s;

	ret = 0;
	i = 0;
	s = 1;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t')
		i++;
	if (str[i] == '-')
	{
		s = -1;
		i++;
	}
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str && str[i] >= '0' && str[i] <= '9')
	{
		ret = ret * 10 + (str[i] - '0');
		i++;
	}
	return (s * ret);
}
