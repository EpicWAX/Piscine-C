int		ft_bouton(int i, int j, int k)
{
	int ret;

	if ((i >= j && i <= k) || (i <= j && i >= k))
		ret = i;
	if ((j >= i && j <= k) || (j <= i && j >= k))
		ret = j;
	if ((k >= j && k <= i) || (k <= j && k >= i))
		ret = k;
	return (ret);
}
